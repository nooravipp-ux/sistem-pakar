
                <div class="row">
                    <div class="col-md-12">
                        <h1 class="text-center">Bab 22 - Pencurian</h1>
                    </div>
                </div>
                <div class="row">
                    <div class="col-md-12">
                        <h2 class="text-center">Pasal 362</h2>
                        <p>Barang siapa mengambil barang sesuatu, yang seluruhnya atau sebagian kepunyaan orang lain, dengan maksud untuk dimiliki secara melawan hukum, diancam karena pencurian, dengan pidana penjara paling lama lima tahun atau pidana denda paling banyak sembilan ratus rupiah.</p>
                    </div>
                </div>
                <div class="row">
                    <div class="col-md-12">
                        <h2 class="text-center">Pasal 363</h2>
                        <p>Diancam dengan pidana penjara paling lama tujuh tahun:</p>
                        <ol>
                            <li>pencurian ternak.</li>
                            <li>pencurian pada waktu ada kebakaran, letusan, banjir gempa bumi, atau gempa laut, gunung meletus, kapal karam, kapal terdampar, kecelakaan kereta api, huru-hara, pemberontakan atau bahaya perang.</li>
                            <li>pencurian di waktu malam dalam sebuah rumah atau pekarangan tertutup yang ada rumahnya, yang dilakukan oleh orang yang ada di situ tidak diketahui atau tidak dikehendaki oleh yang berhak.</li>
                            <li>pencurian yang dilakukan oleh dua orang atau lebih.</li>
                            <li>pencurian yang untuk masuk ke tempat melakukan kejahatan, atau untuk sampai pada barang yang diambil, dilakukan dengan merusak, memotong atau memanjat, atau dengan memakai anak kunci palsu, perintah palsu atau pakaian jabatan palsu.</li>
                        </ol>
                        <p>Jika pencurian yang diterangkan dalam butir 3 disertai dengan salah satu hal dalam butir 4 dan 5, maka diancam dengan pidana penjara paling lama sembilan tahun.</p>
                    </div>
                </div>
                <div class="row">
                    <div class="col-md-12">
                        <h2 class="text-center">Pasal 364</h2>
                        <p>Perbuatan yang diterangkan dalam pasal 362 dan pasal 363 butir 4, begitu pun perbuatan yang diterangkan dalam pasal 363 butir 5, apabila tidak dilakukan dalam sebuah rumah atau pekarangan tertutup yang ada rumahnya, jika harga barang yang dicuri tidak lebih dari dua puluh lima rupiah, diancam karena pencurian ringan dengan pidana penjara paling lama tiga bulan atau pidana denda paling banyak dua ratus lima puluh rupiah.</p>
                    </div>
                </div>
                <div class="row">
                    <div class="col-md-12">
                        <h2 class="text-center">Pasal 365</h2>
                        <p>Diancam dengan pidana penjara paling lama sembilan tahun pencurian yang didahului, disertai atau diikuti dengan kekerasan atau ancaman kekerasan, terhadap orang dengan maksud untuk mempersiapkan atsu mempermudah pencurian, atau dalam hal tertangkap tangan, untuk memungkinkan melarikan diri sendiri atau peserta lainnya, atau untuk tetap menguasai barang yang dicuri.</p>
                        <p> Diancam dengan pidana penjara paling lama dua belas tahun:</p>
                        <ol>
                            <li>jika perbuatan dilakukan pada waktu malam dalam sebuah rumah atau pekarangan tertutup yang ada rumahnya, di berjalan.</li>
                            <li>jika perbuatan dilakukan oleh dua orang atau lebih dengan bersekutu.</li>
                            <li>jika masuk ke tempat melakukan kejahatan dengan merusak atau memanjat atau dengan memakai anak kunci palsu, periniah palsu atau pakaian jabatan palsu.</li>
                            <li>jika perbuatan mengakibatkan luka-luka berat.</li>
                        </ol>
                        <p>Jika perbuatan mengakibatkan kematian maka diancam dengan pidana penjara paling lama lima belas tuhun.</p>
                        <p> Diancam dengan pidana mati atau pidana penjara seumur hidup atau selama waktu tertentu paling lama dua puluh tahun, jika perbuatan mengakihntkan luka berat atau kematian dan dilakukan oleh dua orang atau lebih dengan bersekutu, disertai pula oleh salah satu hal yang diterangkan dalam no. 1 dan 3.</p>
                    </div>
                </div>
                <div class="row">
                    <div class="col-md-12">
                        <h2 class="text-center">Pasal 366</h2>
                        <p>Dalam hal pemidanaan berdasarkan salah satu perbuatan yang dirumuskan dalam pasal 362. 363, dan 865 dapat dijatuhkan pencabutan hak berdasarkan pasal 35 No. 1-4.</p>
                    </div>
                </div>
                <div class="row">
                    <div class="col-md-12">
                        <h2 class="text-center">Pasal 367</h2>
                        <p>Jika pembuat atau pemhantu ciari salah satu kejahatan dalam bab ini adalah suami (istri) dari orang yang terkena kejahatan dan tidak terpisah meja dan ranjang atau terpisah harta kekayaan, maka terhadap pembuat atau pembantu itu tidak mungkin diadakan tuntutan pidana.</p>
                        <p>Jika dia adalah suami (istri) yang terpisah meja dan ranjang atau terpisah harta kekayaan, atau jika dia adalah keluarga sedarah atau semenda, baik dalam garis lurus maupun garis menyimpang derajat kedua maka terhadap orang itu hanya mungkin diadakan penuntutan jika ada pengaduan yang terkena kejahatan.</p>
                    </div>
                </div>